package LogicalProgram;

public class pattern17 {
public static void main(String[] args) {
		
		
		int star=15;
		
	    for (int i =1;i<=15;i++) {
			 
			 for(int j=1;j<=star;j++) {
				 
			     System.out.print(j) ;
			
			 }
			
			 System.out.println();
		    
		     star--;
		
		     }
			 
       }


}
