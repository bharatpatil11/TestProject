package LogicalProgram;

public class pattern14 {
public static void main(String[] args) {// pattern14
		
		
		int star=5;
		
	    for (int i =1;i<=5;i++) {
			 
			 for(int j=1;j<=star;j++) {
				 
			     System.out.print(j) ;
			
			 }
			
			 System.out.println();
		    
		     star--;
		
		     }
			 
       }


}
